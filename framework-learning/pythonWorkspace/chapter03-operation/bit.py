# 60 = 0011 1100 
a = 60
# 13 = 0000 1101 
b = 13
c = 0

c = a & b
print(c) # 12 = 0000 1100

c = a | b
print(c) # 61 = 0011 1101 

c = a ^ b
print(c) # 49 = 0011 0001

c = ~a
print(c) # -61 = 1100 0011

# 高位丢弃，低位补 0
c = a << 2
print(c) # 240 = 1111 0000

# 低位右移，高位补符号位
c = a >> 2
print(c) # 15 = 0000 1111
